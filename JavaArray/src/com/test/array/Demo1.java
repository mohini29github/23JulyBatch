package com.test.array;

public class Demo1 {
	
	public static void main(String[] args) {
		
		String[] str= {"v","e","l","o","c","i","t","y"};
		
		//for loop
		for(int i=0;i<str.length;i++) {
			System.out.print(str[i]);
	}	
		//for-each loop
			for(String j:str) {
				System.out.print(j);
			
		}
		
		
	}

}
